def test_separate_thread_ids_isolate_checkpoints(tmp_path):
    """Two different conversation_ids must produce independent checkpoints
    — critical for the cron path which uses ``scheduled:{date}:{run_id}`` and
    must NEVER interfere with a parallel user conversation."""
    memory = _make_memory(tmp_path)
    saver = memory.langgraph_checkpointer

    async def _go():
        app = _build_review_only_graph(saver)
        # Thread A pauses
        cfg_a = {"configurable": {"thread_id": "t-iso-A"}}
        async for _ in app.astream(
            {"pending_review": _draft("a1"), "auto_approve": False},
            config=cfg_a,
            stream_mode="updates",
        ):
            pass
        # Thread B independently auto-approves
        cfg_b = {"configurable": {"thread_id": "t-iso-B"}}
        async for _ in app.astream(
            {"pending_review": _draft("b1"), "auto_approve": True},
            config=cfg_b,
            stream_mode="updates",
        ):
            pass
        # Now resume A — should see ONLY a1, not b1
        async for _ in app.astream(
            Command(resume={"action": "approve"}),
            config=cfg_a,
            stream_mode="updates",
        ):
            pass
        snap_a = await app.aget_state(cfg_a)
        snap_b = await app.aget_state(cfg_b)
        return snap_a.values, snap_b.values

    values_a, values_b = asyncio.run(_go())
    assert [d.email_id for d in (values_a.get("review_decisions") or [])] == ["a1"]
    assert [d.email_id for d in (values_b.get("review_decisions") or [])] == ["b1"]


# ─── Direct adapter API smoke tests (no graph) ──────────────────────────────


def test_aget_tuple_returns_none_for_unknown_thread(tmp_path):
    """The adapter must return ``None`` (not raise) for a thread_id that
    has never been written to. LangGraph relies on this for fresh threads."""
    memory = _make_memory(tmp_path)
    saver = memory.langgraph_checkpointer

    async def _go():
        return await saver.aget_tuple({"configurable": {"thread_id": "never-existed"}})

    result = asyncio.run(_go())
    assert result is None


def test_alist_returns_empty_for_unknown_thread(tmp_path):
    memory = _make_memory(tmp_path)
    saver = memory.langgraph_checkpointer

    async def _go():
        return await saver.alist({"configurable": {"thread_id": "ghost"}})

    result = asyncio.run(_go())
    assert result == []


def test_aput_then_aget_round_trips_minimal_checkpoint(tmp_path):
    """Direct aput → aget_tuple round trip with a minimal valid checkpoint
    payload. Verifies the JSON+pickle serialization end-to-end."""
    memory = _make_memory(tmp_path)
    saver = memory.langgraph_checkpointer
    config = {"configurable": {"thread_id": "t-direct-roundtrip"}}
    checkpoint = {
        "id": "ckpt-1",
        "ts": "2026-05-20T00:00:00Z",
        "v": 1,
        "channel_values": {"foo": "bar"},
        "channel_versions": {"foo": 1},
        "versions_seen": {},
    }

    async def _go():
        await saver.aput(config, checkpoint, metadata={"source": "test"})
        result = await saver.aget_tuple(config)
        return result

    tup = asyncio.run(_go())
    assert tup is not None
    assert tup.checkpoint["id"] == "ckpt-1"
    assert tup.checkpoint["channel_values"] == {"foo": "bar"}
